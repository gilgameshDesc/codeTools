    public List<String> scan(String pattern, Long scanCount) {
        List<String> keys = redisTemplate.execute((RedisConnection connection) -> {
            List<String> keySet = CollUtil.newArrayList();
            //定义起始游标，获取lettuce原生引用，定义scan参数
            ScanCursor scanCursor = ScanCursor.INITIAL;
            RedisKeyAsyncCommands commands = (RedisKeyAsyncCommands) connection.getNativeConnection();
            ScanArgs scanArgs = ScanArgs.Builder.limit(scanCount).match(pattern);
            int i = 0;
            try {
                do {
                    //最少scan一次，当返回不为空时将扫描到的key添加到统一key列表中
                    KeyScanCursor<byte[]> keyScanCursor = (KeyScanCursor) commands.scan(scanCursor, scanArgs).get();
                    if (keyScanCursor != null) {
                        if (CollUtil.isNotEmpty(keyScanCursor.getKeys())) {
                            keyScanCursor.getKeys().forEach(b -> keySet.add(new String(b)));
                            scanCursor = ScanCursor.FINISHED;
                        }
                        scanCursor = keyScanCursor;
//                        scanCursor = ScanCursor.FINISHED;
                    } else {
                        scanCursor = ScanCursor.FINISHED;
                    }
                } while (!scanCursor.isFinished());
            } catch (Exception e) {
                e.printStackTrace();
            }
            return keySet;
        });
        return keys;
    }

    /**
     * 分页查询 key
     *
     * @param patternKey key
     * @param page       页码
     * @param size       每页数目
     * @return /
     */
    public List<String> findKeysForPage(String patternKey, int page, int size) {
        ScanOptions options = ScanOptions.scanOptions().match(patternKey).build();
        RedisConnectionFactory factory = redisTemplate.getConnectionFactory();
        RedisConnection rc = Objects.requireNonNull(factory).getConnection();
        Cursor<byte[]> cursor = rc.scan(options);
        List<String> result = new ArrayList<>(size);
        int tmpIndex = 0;
        int fromIndex = page * size;
        int toIndex = page * size + size;
        while (cursor.hasNext()) {
            if (tmpIndex >= fromIndex && tmpIndex < toIndex) {
                result.add(new String(cursor.next()));
                tmpIndex++;
                continue;
            }
            // 获取到满足条件的数据后,就可以退出了
            if (tmpIndex >= toIndex) {
                break;
            }
            tmpIndex++;
            cursor.next();
        }
        try {
            RedisConnectionUtils.releaseConnection(rc, factory);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return result;
    }

